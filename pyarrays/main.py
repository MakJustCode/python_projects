#Function 
def arrayLoop():
    
    #Array 
    A = ["Jack", "And", "Jill", "Ran", "Up", "The", "Hill"]

    #Length
    length = len(A) 

    #For Loop 
    for i in range(length): 
        print(A[i]) 

print (arrayLoop()) 


